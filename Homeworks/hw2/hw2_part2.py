def main():
    price = int(input("What is the price of the book? "))
    numCopies = int(input("How many copies of this book would you like? "))
    
    tax = 6.5 * (price * numCopies)
    costShipping = 1.95 * numCopies
    totalCost = tax + costShipping + (numCopies * price)
    print("Your total cost for the books is: $", totalCost)


main()
